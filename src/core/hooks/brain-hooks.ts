/**
 * Brain-side hook scripts for Codi Brain Week 2B.
 *
 * SessionStart — flushes `.codi/brain-outbox/` and injects /hot + recent
 *                decisions into the agent context as `additionalContext`.
 * Stop         — parses CODI-* markers (L1) and, if opt-in via
 *                `.codi/config.yaml` `brain.auto_extract: true`, redacts the
 *                transcript + calls Gemini (L3) with evidence-quote
 *                verification + dedup against L1.
 * PostCommit   — on PostToolUse for `git commit`, POSTs /vault/reconcile.
 *
 * Written to `.codi/hooks/` by `codi generate`; wired into
 * `.claude/settings.json` by `src/adapters/claude-code.ts`.
 *
 * NOTE: REDACTION_PATTERNS below MUST be kept in sync with
 *       src/brain-client/redactor-patterns.ts (tested in
 *       tests/unit/core/hooks/brain-hooks.test.ts via the stop script).
 */

