import { describe, it, expect, beforeEach, afterEach } from "vitest";
import { mkdir, writeFile, rm } from "node:fs/promises";
import { join } from "node:path";
import { tmpdir } from "node:os";
import { claudeCodeAdapter } from "#src/adapters/claude-code.js";
import {
  CONTEXT_TOKENS_LARGE,
  PROJECT_NAME,
  PROJECT_NAME_DISPLAY,
  MANIFEST_FILENAME,
} from "#src/constants.js";
import { createMockConfig } from "./mock-config.js";

describe("claude-code adapter", () => {
  let tmpDir: string;

  beforeEach(async () => {
    tmpDir = join(
      tmpdir(),
      `${PROJECT_NAME}-test-claude-${Date.now()}-${Math.random().toString(36).slice(2)}`,
    );
    await mkdir(tmpDir, { recursive: true });
  });

  afterEach(async () => {
    await rm(tmpDir, { recursive: true, force: true });
  });

  // ── identity ──────────────────────────────────────────────────────

  it("has correct id and name", () => {
    expect(claudeCodeAdapter.id).toBe("claude-code");
    expect(claudeCodeAdapter.name).toBe("Claude Code");
  });

  // ── detect() ──────────────────────────────────────────────────────

  describe("detect()", () => {
    it("returns true when CLAUDE.md exists", async () => {
      await writeFile(join(tmpDir, "CLAUDE.md"), "# Test");
      expect(await claudeCodeAdapter.detect(tmpDir)).toBe(true);
    });

    it("returns true when .claude/ directory exists", async () => {
      await mkdir(join(tmpDir, ".claude"), { recursive: true });
      expect(await claudeCodeAdapter.detect(tmpDir)).toBe(true);
    });

    it("returns false for empty directory", async () => {
      expect(await claudeCodeAdapter.detect(tmpDir)).toBe(false);
    });
  });

  // ── generate() — CLAUDE.md ────────────────────────────────────────

  describe("generate() — CLAUDE.md", () => {
    it("produces CLAUDE.md with generated footer", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});

      const mainFile = files.find((f) => f.path === "CLAUDE.md");
      expect(mainFile).toBeDefined();
      expect(mainFile!.content).toContain(`Generated by ${PROJECT_NAME_DISPLAY}`);
      expect(mainFile!.content).toMatch(
        new RegExp(`<!-- Generated by ${PROJECT_NAME_DISPLAY}.*-->\\s*$`),
      );
    });

    it("includes project overview section", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});
      const mainFile = files.find((f) => f.path === "CLAUDE.md")!;

      expect(mainFile.content).toContain("## Project Overview");
      expect(mainFile.content).toContain("**Project:** test-project");
      expect(mainFile.content).toContain(`**Managed by:** [${PROJECT_NAME_DISPLAY}]`);
    });

    it("includes permission/flag instructions", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});
      const mainFile = files.find((f) => f.path === "CLAUDE.md")!;

      expect(mainFile.content).toContain("## Permissions");
      expect(mainFile.content).toContain("Do NOT execute shell commands.");
      expect(mainFile.content).toContain("Do NOT delete files.");
      expect(mainFile.content).toContain("Keep source code files under 700 lines.");
      expect(mainFile.content).toContain("Write tests for all new code.");
    });

    it("includes workflow section", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});
      const mainFile = files.find((f) => f.path === "CLAUDE.md")!;

      expect(mainFile.content).toContain("## Workflow");
      expect(mainFile.content).toContain("### Before Writing Code");
      expect(mainFile.content).toContain("### Self-Evaluation Checklist");
    });

    it("has a non-empty hash", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});
      const mainFile = files.find((f) => f.path === "CLAUDE.md")!;

      expect(mainFile.hash).toBeTruthy();
      expect(typeof mainFile.hash).toBe("string");
    });

    it("injects self-dev warning when project name is codi", async () => {
      const config = createMockConfig({
        manifest: { name: PROJECT_NAME, version: "1", agents: [] },
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const content = files.find((f) => f.path === "CLAUDE.md")!.content;

      expect(content).toContain("## Self-Development Mode");
      expect(content).toContain("src/templates/");
    });

    it("does not inject self-dev warning for non-codi projects", async () => {
      const config = createMockConfig({
        manifest: { name: "my-app", version: "1", agents: [] },
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const content = files.find((f) => f.path === "CLAUDE.md")!.content;

      expect(content).not.toContain("## Self-Development Mode");
    });
  });

  // ── generate() — rule files ────────────────────────────────────────

  describe("generate() — rule files", () => {
    it("produces rule files in .claude/rules/", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});

      const ruleFiles = files.filter((f) => f.path.startsWith(".claude/rules/"));
      expect(ruleFiles).toHaveLength(2);
      expect(ruleFiles.map((f) => f.path)).toContain(".claude/rules/code-style.md");
      expect(ruleFiles.map((f) => f.path)).toContain(".claude/rules/testing.md");
    });

    it("rule files without scope have no YAML frontmatter", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});

      const codeStyleRule = files.find((f) => f.path === ".claude/rules/code-style.md")!;
      const withoutFooter = codeStyleRule.content.replace(
        new RegExp(`\n\n<!-- Generated by ${PROJECT_NAME_DISPLAY}.*?-->\n$`),
        "",
      );
      expect(withoutFooter).not.toMatch(/^---/);
    });

    it("rule files with scope emit paths frontmatter", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});

      const testingRule = files.find((f) => f.path === ".claude/rules/testing.md")!;
      expect(testingRule.content).toMatch(/^---\npaths:\n\s+-\s+"[^"]+"/);
      expect(testingRule.content).toContain('- "**/*.test.ts"');
    });

    it("alwaysApply: false + language derives paths frontmatter for Claude Code", async () => {
      const config = createMockConfig({
        rules: [
          {
            name: "typescript-conventions",
            description: "TS rules",
            content: "Use strict mode.",
            language: "typescript",
            priority: "medium",
            scope: [],
            alwaysApply: false,
            managedBy: "user",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const ruleFile = files.find((f) => f.path === ".claude/rules/typescript-conventions.md")!;
      expect(ruleFile.content).toContain('- "**/*.ts"');
      expect(ruleFile.content).toContain('- "**/*.tsx"');
    });

    it("alwaysApply: false without language or scope emits no frontmatter", async () => {
      const config = createMockConfig({
        rules: [
          {
            name: "agent-usage",
            description: "Agent guidelines",
            content: "Use agents wisely.",
            priority: "medium",
            scope: [],
            alwaysApply: false,
            managedBy: "user",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const ruleFile = files.find((f) => f.path === ".claude/rules/agent-usage.md")!;
      expect(ruleFile.content).not.toMatch(/^---/);
    });

    it("explicit scope takes priority over language-derived paths", async () => {
      const config = createMockConfig({
        rules: [
          {
            name: "python-conventions",
            description: "Python rules",
            content: "Follow PEP8.",
            language: "python",
            priority: "medium",
            scope: ["src/**/*.py"],
            alwaysApply: false,
            managedBy: "user",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const ruleFile = files.find((f) => f.path === ".claude/rules/python-conventions.md")!;
      expect(ruleFile.content).toContain('"src/**/*.py"');
      expect(ruleFile.content).not.toContain('"**/*.py"');
    });

    it("alwaysApply: true with language emits no frontmatter", async () => {
      const config = createMockConfig({
        rules: [
          {
            name: "golang-conventions",
            description: "Go rules",
            content: "Use gofmt.",
            language: "golang",
            priority: "medium",
            scope: [],
            alwaysApply: true,
            managedBy: "user",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const ruleFile = files.find((f) => f.path === ".claude/rules/golang-conventions.md")!;
      expect(ruleFile.content).not.toMatch(/^---/);
    });

    it("rule files contain their name as heading and content", async () => {
      const config = createMockConfig();
      const files = await claudeCodeAdapter.generate(config, {});

      const codeStyleRule = files.find((f) => f.path === ".claude/rules/code-style.md")!;
      expect(codeStyleRule.content).toContain(`# (${PROJECT_NAME}-rule) Code Style`);
      expect(codeStyleRule.content).toContain("Use 2-space indentation and single quotes.");
    });
  });

  // ── generate() — skill files ───────────────────────────────────────

  describe("generate() — skill files", () => {
    it("produces skill files in .claude/skills/", async () => {
      const config = createMockConfig({
        skills: [
          {
            name: "code-review",
            description: "Reviews code for quality",
            content: "Perform thorough code reviews.",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});

      const skillFiles = files.filter((f) => f.path.startsWith(".claude/skills/"));
      const skillMds = skillFiles.filter((f) => f.path.endsWith("SKILL.md"));
      expect(skillMds).toHaveLength(1);
      expect(skillMds[0]!.path).toBe(".claude/skills/code-review/SKILL.md");
      expect(skillMds[0]!.content).toContain("code-review");
      expect(skillMds[0]!.content).toContain("Perform thorough code reviews.");
      // Skeleton directories created
      expect(skillFiles.some((f) => f.path.includes("scripts/.gitkeep"))).toBe(true);
      expect(skillFiles.some((f) => f.path.includes("references/.gitkeep"))).toBe(true);
      expect(skillFiles.some((f) => f.path.includes("assets/.gitkeep"))).toBe(true);
    });

    it("produces no skill files when skills array is empty", async () => {
      const config = createMockConfig({ skills: [] });
      const files = await claudeCodeAdapter.generate(config, {});

      const skillFiles = files.filter((f) => f.path.startsWith(".claude/skills/"));
      expect(skillFiles).toHaveLength(0);
    });
  });

  // ── generate() — agent files ───────────────────────────────────────

  describe("generate() — agent files", () => {
    it("produces agent files in .claude/agents/", async () => {
      const config = createMockConfig({
        agents: [
          {
            name: "security-scanner",
            description: "Scans for vulnerabilities",
            content: "Run SAST and DAST scans.",
            tools: ["Bash", "Read"],
            model: "claude-sonnet-4-20250514",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});

      const agentFiles = files.filter((f) => f.path.startsWith(".claude/agents/"));
      expect(agentFiles).toHaveLength(1);
      expect(agentFiles[0]!.path).toBe(".claude/agents/security-scanner.md");
    });

    it("agent files contain YAML frontmatter with metadata", async () => {
      const config = createMockConfig({
        agents: [
          {
            name: "security-scanner",
            description: "Scans for vulnerabilities",
            content: "Run SAST and DAST scans.",
            tools: ["Bash", "Read"],
            model: "claude-sonnet-4-20250514",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const agentFile = files.find((f) => f.path.startsWith(".claude/agents/"))!;

      expect(agentFile.content).toContain("name: security-scanner");
      expect(agentFile.content).toContain(`description: Scans for vulnerabilities`);
      expect(agentFile.content).toContain("tools: Bash, Read");
      expect(agentFile.content).toContain("model: claude-sonnet-4-20250514");
      expect(agentFile.content).toContain("Run SAST and DAST scans.");
    });

    it("agent files omit tools and model when not specified", async () => {
      const config = createMockConfig({
        agents: [
          {
            name: "helper",
            description: "A helper agent",
            content: "Help with tasks.",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const agentFile = files.find((f) => f.path.startsWith(".claude/agents/"))!;

      expect(agentFile.content).not.toContain("tools:");
      expect(agentFile.content).not.toContain("model:");
    });

    it("agent files emit disallowedTools, maxTurns, and effort when specified", async () => {
      const config = createMockConfig({
        agents: [
          {
            name: "safe-agent",
            description: "A safety-bounded agent",
            content: "Act with caution.",
            tools: ["Read", "Grep"],
            disallowedTools: ["Bash", "Write"],
            model: "claude-haiku-4-5-20251001",
            maxTurns: 10,
            effort: "low",
          },
        ],
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const agentFile = files.find((f) => f.path.startsWith(".claude/agents/"))!;

      expect(agentFile.content).toContain("disallowedTools: Bash, Write");
      expect(agentFile.content).toContain("maxTurns: 10");
      expect(agentFile.content).toContain("effort: low");
    });

    it("produces no agent files when agents array is empty", async () => {
      const config = createMockConfig({ agents: [] });
      const files = await claudeCodeAdapter.generate(config, {});

      const agentFiles = files.filter((f) => f.path.startsWith(".claude/agents/"));
      expect(agentFiles).toHaveLength(0);
    });
  });

  // ── generate() — MCP config ────────────────────────────────────────

  describe("generate() — MCP config", () => {
    it("produces .mcp.json when MCP servers are configured", async () => {
      const config = createMockConfig({
        mcp: {
          servers: {
            "my-server": {
              type: "stdio",
              command: "npx",
              args: ["-y", "my-mcp-server"],
              enabled: true,
            },
          },
        },
      });
      const files = await claudeCodeAdapter.generate(config, {});

      const mcpFile = files.find((f) => f.path === ".mcp.json");
      expect(mcpFile).toBeDefined();

      const parsed = JSON.parse(mcpFile!.content);
      expect(parsed.mcpServers["my-server"]).toBeDefined();
      expect(parsed.mcpServers["my-server"].command).toBe("npx");
      expect(parsed.mcpServers["my-server"].args).toEqual(["-y", "my-mcp-server"]);
    });

    it("strips the enabled field from MCP server entries", async () => {
      const config = createMockConfig({
        mcp: {
          servers: {
            "my-server": {
              type: "stdio",
              command: "npx",
              args: [],
              enabled: true,
            },
          },
        },
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const mcpFile = files.find((f) => f.path === ".mcp.json")!;
      const parsed = JSON.parse(mcpFile.content);

      expect(parsed.mcpServers["my-server"]).not.toHaveProperty("enabled");
    });

    it("excludes disabled MCP servers", async () => {
      const config = createMockConfig({
        mcp: {
          servers: {
            active: { type: "stdio", command: "npx", args: [], enabled: true },
            disabled: {
              type: "stdio",
              command: "npx",
              args: [],
              enabled: false,
            },
          },
        },
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const mcpFile = files.find((f) => f.path === ".mcp.json")!;
      const parsed = JSON.parse(mcpFile.content);

      expect(parsed.mcpServers).toHaveProperty("active");
      expect(parsed.mcpServers).not.toHaveProperty("disabled");
    });

    it("does not produce mcp.json when no servers are configured", async () => {
      const config = createMockConfig({ mcp: { servers: {} } });
      const files = await claudeCodeAdapter.generate(config, {});

      const mcpFile = files.find((f) => f.path === ".mcp.json");
      expect(mcpFile).toBeUndefined();
    });

    it("does not produce mcp.json when all servers are disabled", async () => {
      const config = createMockConfig({
        mcp: {
          servers: {
            disabled: {
              type: "stdio",
              command: "npx",
              args: [],
              enabled: false,
            },
          },
        },
      });
      const files = await claudeCodeAdapter.generate(config, {});

      const mcpFile = files.find((f) => f.path === ".mcp.json");
      expect(mcpFile).toBeUndefined();
    });
  });

  // ── generate() — heartbeat hook scripts ──────────────────────────

  describe("generate() — heartbeat hook scripts", () => {
    it("includes the skill-tracker script in generated files", async () => {
      const config = createMockConfig({});
      const files = await claudeCodeAdapter.generate(config, {});

      const tracker = files.find((f) => f.path.endsWith("codi-skill-tracker.cjs"));
      expect(tracker).toBeDefined();
      expect(tracker!.path).toBe(".codi/hooks/codi-skill-tracker.cjs");
      expect(tracker!.content).toContain("SKILL.md");
    });

    it("includes the skill-observer script in generated files", async () => {
      const config = createMockConfig({});
      const files = await claudeCodeAdapter.generate(config, {});

      const observer = files.find((f) => f.path.endsWith("codi-skill-observer.cjs"));
      expect(observer).toBeDefined();
      expect(observer!.path).toBe(".codi/hooks/codi-skill-observer.cjs");
      expect(observer!.content).toContain("CODI-OBSERVATION");
    });

    it("hook scripts have non-empty content and a hash", async () => {
      const config = createMockConfig({});
      const files = await claudeCodeAdapter.generate(config, {});

      for (const suffix of ["codi-skill-tracker.cjs", "codi-skill-observer.cjs"]) {
        const f = files.find((file) => file.path.endsWith(suffix));
        expect(f).toBeDefined();
        expect(f!.content.length).toBeGreaterThan(0);
        expect(f!.hash).toBeTruthy();
      }
    });
  });

  // ── generate() — settings.json ─────────────────────────────────────

  describe("generate() — settings.json (buildSettingsJson)", () => {
    it("always generates settings.json with heartbeat hooks even when no permission flags are set", async () => {
      const config = createMockConfig({
        flags: {
          lint_on_save: {
            value: true,
            mode: "enabled",
            source: MANIFEST_FILENAME,
            locked: false,
          },
        },
      });
      const files = await claudeCodeAdapter.generate(config, {});

      const settingsFile = files.find((f) => f.path === ".claude/settings.json");
      expect(settingsFile).toBeDefined();
      const parsed = JSON.parse(settingsFile!.content);
      expect(parsed.hooks).toBeDefined();
      expect(parsed.permissions).toBeUndefined();
    });

    it("settings.json InstructionsLoaded hook points to skill-tracker with async: true", async () => {
      const config = createMockConfig({});
      const files = await claudeCodeAdapter.generate(config, {});

      const settingsFile = files.find((f) => f.path === ".claude/settings.json");
      const parsed = JSON.parse(settingsFile!.content);
      const entry = parsed.hooks.InstructionsLoaded?.[0];
      expect(entry).toBeDefined();
      expect(entry.matcher).toBe("");
      const hook = entry.hooks?.[0];
      expect(hook).toBeDefined();
      expect(hook.type).toBe("command");
      expect(hook.command).toContain("codi-skill-tracker.cjs");
      expect(hook.async).toBe(true);
      expect(hook.timeout).toBeGreaterThan(0);
    });

    it("settings.json Stop hook points to skill-observer without async flag", async () => {
      const config = createMockConfig({});
      const files = await claudeCodeAdapter.generate(config, {});

      const settingsFile = files.find((f) => f.path === ".claude/settings.json");
      const parsed = JSON.parse(settingsFile!.content);
      const entry = parsed.hooks.Stop?.[0];
      expect(entry).toBeDefined();
      expect(entry.matcher).toBe("");
      const hook = entry.hooks?.[0];
      expect(hook).toBeDefined();
      expect(hook.type).toBe("command");
      expect(hook.command).toContain("codi-skill-observer.cjs");
      expect(hook.async).toBeUndefined();
      expect(hook.timeout).toBeGreaterThan(0);
    });

    it("generates permissions.deny for allow_force_push: false", async () => {
      const config = createMockConfig({
        flags: {
          allow_force_push: {
            value: false,
            mode: "enforced",
            source: MANIFEST_FILENAME,
            locked: false,
          },
        },
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const settingsFile = files.find((f) => f.path === ".claude/settings.json");
      expect(settingsFile).toBeDefined();
      const parsed = JSON.parse(settingsFile!.content);
      expect(parsed.permissions.deny).toContain("Bash(git push --force *)");
      expect(parsed.permissions.deny).toContain("Bash(git push -f *)");
    });

    it("generates permissions.deny for allow_shell_commands: false", async () => {
      const config = createMockConfig({
        flags: {
          allow_shell_commands: {
            value: false,
            mode: "enforced",
            source: MANIFEST_FILENAME,
            locked: false,
          },
        },
      });
      const files = await claudeCodeAdapter.generate(config, {});
      const settingsFile = files.find((f) => f.path === ".claude/settings.json");
      expect(settingsFile).toBeDefined();
      const parsed = JSON.parse(settingsFile!.content);
      expect(parsed.permissions.deny).toContain("Bash");
    });

    it("generates settings.json with hooks when flags object is empty", async () => {
      const config = createMockConfig({ flags: {} });
      const files = await claudeCodeAdapter.generate(config, {});

      const settingsFile = files.find((f) => f.path === ".claude/settings.json");
      expect(settingsFile).toBeDefined();
      const parsed = JSON.parse(settingsFile!.content);
      expect(parsed.hooks).toBeDefined();
    });
  });

  // ── generate() — capabilities ──────────────────────────────────────

  describe("capabilities", () => {
    it("exposes maxContextTokens as CONTEXT_TOKENS_LARGE", () => {
      expect(claudeCodeAdapter.capabilities.maxContextTokens).toBe(CONTEXT_TOKENS_LARGE);
      expect(claudeCodeAdapter.capabilities.maxContextTokens).toBe(200_000);
    });

    it("supports rules, skills, mcp, agents, and progressiveLoading", () => {
      expect(claudeCodeAdapter.capabilities.rules).toBe(true);
      expect(claudeCodeAdapter.capabilities.skills).toBe(true);
      expect(claudeCodeAdapter.capabilities.mcp).toBe(true);
      expect(claudeCodeAdapter.capabilities.agents).toBe(true);
      expect(claudeCodeAdapter.capabilities.progressiveLoading).toBe(true);
    });

    it("does not use frontmatter", () => {
      expect(claudeCodeAdapter.capabilities.frontmatter).toBe(false);
    });
  });

  // ── generate() — brand-category skills ──────────────────────────────

  it("generates brand files from brand-category skills", async () => {
    const config = createMockConfig({
      skills: [
        {
          name: "my-brand",
          description: "Brand identity",
          content: "Brand content here",
          category: "brand",
        },
      ],
    });
    const files = await claudeCodeAdapter.generate(config, {
      projectRoot: tmpDir,
    });

    const brandFile = files.find((f) => f.path.includes("brands/my-brand.md"));
    expect(brandFile).toBeDefined();
    expect(brandFile!.content).toContain("Brand content here");
  });

  // ── generate() — paths ─────────────────────────────────────────────

  describe("paths", () => {
    it("has correct path configuration", () => {
      expect(claudeCodeAdapter.paths.configRoot).toBe(".claude");
      expect(claudeCodeAdapter.paths.rules).toBe(".claude/rules");
      expect(claudeCodeAdapter.paths.skills).toBe(".claude/skills");
      expect(claudeCodeAdapter.paths.agents).toBe(".claude/agents");
      expect(claudeCodeAdapter.paths.instructionFile).toBe("CLAUDE.md");
      expect(claudeCodeAdapter.paths.mcpConfig).toBe(".mcp.json");
    });
  });
});
